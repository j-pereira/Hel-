    public ResultadoPesquisa(List<Usuario> listaUsuario){
        this();
        
        this.listaUsuario = listaUsuario;
        preencheLista();
        
    }
    
    public void preencheLista(){
        DefaultListModel listModel = new DefaultListModel<String>();
        
        if(listaUsuario.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nenhum resultado encontrado."  ,"Resultado da pesquisa",JOptionPane.PLAIN_MESSAGE);
        
        }else{
            for(int i=0; i<listaUsuario.size(); i++){
                listModel.add(i, this.listaUsuario.get(i).getNome());
            }
        
            listNome.setModel(listModel);
        }
        
       
    }
    
    public void acessaTelaUsuario(int indice){
        Usuario usuario = this.listaUsuario.get(indice);
        TelaUsuario tela = new TelaUsuario(usuario);
        tela.setVisible(true);
        dispose();
    }

